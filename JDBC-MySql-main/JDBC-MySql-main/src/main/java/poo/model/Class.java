package poo.model;

public class Class {
    private String className;
    private int level;
    private String abiCla;

    
    public String getClassName() {
        return className;
    }
    public void setClassName(String className) {
        this.className = className;
    }
    public int getLevel() {
        return level;
    }
    public void setLevel(int level) {
        this.level = level;
    }
    public String getAbiCla() {
        return abiCla;
    }
    public void setAbiCla(String abiCla) {
        this.abiCla = abiCla;
    }

}